static int slcd_inited = 0;

static int frd397bp4511_power_init(struct lcd_device *ld)
{
    int ret;

    if(GPIO_LCD_RST != -1) {
        ret = gpio_request(GPIO_LCD_RST, "lcd rst");
        if (ret) {
            printk(KERN_ERR "can's request lcd rst\n");
            return ret;
        }
    }

    if(GPIO_LCD_CS != -1) {
        ret = gpio_request(GPIO_LCD_CS, "lcd cs");
        if (ret) {
            printk(KERN_ERR "can's request lcd cs\n");
            return ret;
        }
    }

    if(GPIO_LCD_RD != -1) {
        ret = gpio_request(GPIO_LCD_RD, "lcd rd");
        if (ret) {
            printk(KERN_ERR "can's request lcd rd\n");
            return ret;
        }
    }

    if(GPIO_LCD_VDD_EN != -1) {
        ret = gpio_request(GPIO_LCD_VDD_EN, "lcd vdd enable");
        if (ret) {
            printk(KERN_ERR "can's request lcd vdd enable\n");
            return ret;
        }
   }

    printk("%s: set slcd_inited = 1\n", __FUNCTION__);
    slcd_inited = 1;

    return 0;
}

static int frd397bp4511_power_reset(struct lcd_device *ld)
{
    if (!slcd_inited && frd397bp4511_power_init(ld))
        return -EFAULT;

    if (GPIO_LCD_CS != -1) {
        gpio_direction_output(GPIO_LCD_CS, 1);
    }

    if (GPIO_LCD_RD != -1) {
        gpio_direction_output(GPIO_LCD_RD, 1);
    }

    if(GPIO_LCD_RST != -1) {
        gpio_direction_output(GPIO_LCD_RST, 0);
        mdelay(20);
        gpio_direction_output(GPIO_LCD_RST, 1);
        mdelay(20);
    }

    gpio_direction_output(GPIO_LCD_CS, 0);
    return 0;
}

int frd397bp4511_power_on(struct lcd_device *ld, int enable)
{
    if (!slcd_inited && frd397bp4511_power_init(ld))
        return -EFAULT;

    switch (enable) {
        /* backlight enable */
        case FB_BLANK_UNBLANK:
            break;

        /* power enable */
        case FB_BLANK_NORMAL:
            gpio_direction_output(GPIO_LCD_VDD_EN, 1);

            mdelay(140);

            if (GPIO_LCD_CS != -1) {
                gpio_direction_output(GPIO_LCD_CS, 1);
            }

            if (GPIO_LCD_RD != -1) {
                gpio_direction_output(GPIO_LCD_RD, 1);
            }

            if(GPIO_LCD_RST != -1) {
                gpio_direction_output(GPIO_LCD_RST, 0);
                mdelay(20);
                gpio_direction_output(GPIO_LCD_RST, 1);
                mdelay(20);
            } else {
                mdelay(40);
            }

            gpio_direction_output(GPIO_LCD_CS, 0);
            break;

        /* backlight disable */
        case FB_BLANK_VSYNC_SUSPEND:
        case FB_BLANK_HSYNC_SUSPEND:
            break;

        /* power and backlight disable */
        default:
            gpio_direction_output(GPIO_LCD_CS, 1);
            gpio_direction_output(GPIO_LCD_VDD_EN, 0);
            break;

    }

    return 0;
}

